function buildMap(
  errors: Record<string, number>,
): Record<number, ErrorEntry> {
  const map: Record<number, ErrorEntry> = {};
  for (const [name, code] of Object.entries(errors)) {
    map[code] = { name, msg: '' };
  }
  return map;
}

