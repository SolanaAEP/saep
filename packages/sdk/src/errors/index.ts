function buildFullMap(): Record<string, Record<number, ErrorEntry>> {
  const result: Record<string, Record<number, ErrorEntry>> = {};
  for (const [program, def] of Object.entries(PROGRAM_ERROR_DEFS)) {
    const map: Record<number, ErrorEntry> = {};
    for (const [name, code] of Object.entries(def.errors)) {
      map[code] = { name, msg: def.msgs[code] ?? '' };
    }
    result[program] = map;
  }
  return result;
}

export const SAEP_ERRORS: Record<string, Record<number, ErrorEntry>> = buildFullMap();

// When programName is omitted, searches all programs and returns the first match.
// Error codes overlap across programs so the result may be ambiguous without a program hint.
export function decodeSaepError(code: number, programName?: string): SaepError | null {
  if (programName) {
    const entry = SAEP_ERRORS[programName]?.[code];
    if (!entry) return null;
    return { code, ...entry, program: programName };
  }

  for (const [program, map] of Object.entries(SAEP_ERRORS)) {
    const entry = map[code];
    if (entry) return { code, ...entry, program };
  }

  return null;
}
